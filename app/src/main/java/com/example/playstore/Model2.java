package com.example.playstore;

public class Model2 {
    int Image2;
    String Name;
    int Rating;

    public Model2(int image2, String name, int rating) {
        Image2 = image2;
        Name = name;
        Rating = rating;
    }

    public int getImage2() {
        return Image2;
    }

    public String getName() {
        return Name;
    }

    public int getRating() {
        return Rating;
    }
}
